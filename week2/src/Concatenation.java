public class Concatenation {
    public static void main(String[] args) {
        String color = "red";
        System.out.println("my favorite color is  "+ color);  
        
        int money = 2_000_000;
        System.out.println("I won "+ money + " dollors.");  

        System.out.println(color + " "+ money);  

        System.out.println("my favorite color is  "+ color + ". I won "+ money + " dollors.");  
    }
}
